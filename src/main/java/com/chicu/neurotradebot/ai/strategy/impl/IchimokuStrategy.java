@Slf4j
@Component
@RequiredArgsConstructor
public class IchimokuStrategy implements AiStrategy {

    private final MarketCandleService candleService;
    private final OrderService orderService; // –î–ª—è —Ä–∞–∑–º–µ—â–µ–Ω–∏—è –æ—Ä–¥–µ—Ä–æ–≤
    private final TelegramNotificationService notificationService; // –î–ª—è —É–≤–µ–¥–æ–º–ª–µ–Ω–∏–π
    private IchimokuConfig config = new IchimokuConfig(); // –ö–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—è –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é

    @Override
    public String getName() {
        return "Ichimoku";
    }

    @Override
    public Signal analyze(List<MarketCandle> candles) {
        if (candles.size() < config.getBaseLinePeriod() + 1) {
            log.warn("üìâ –ù–µ–¥–æ—Å—Ç–∞—Ç–æ—á–Ω–æ –¥–∞–Ω–Ω—ã—Ö –¥–ª—è Ichimoku (–º–∏–Ω–∏–º—É–º {} –±–∞—Ä–æ–≤)", config.getBaseLinePeriod() + 1);
            return Signal.HOLD;  // –ï—Å–ª–∏ –¥–∞–Ω–Ω—ã—Ö –Ω–µ–¥–æ—Å—Ç–∞—Ç–æ—á–Ω–æ –¥–ª—è –∞–Ω–∞–ª–∏–∑–∞, –≤–æ–∑–≤—Ä–∞—â–∞–µ–º HOLD
        }

        BarSeries series = candleService.buildBarSeries(candles);

        IchimokuTenkanSenIndicator tenkanSen = new IchimokuTenkanSenIndicator(series, config.getConversionLinePeriod());
        IchimokuKijunSenIndicator kijunSen = new IchimokuKijunSenIndicator(series, config.getBaseLinePeriod());
        ClosePriceIndicator close = new ClosePriceIndicator(series);

        int lastIndex = series.getEndIndex();
        double tenkan = tenkanSen.getValue(lastIndex).doubleValue();
        double kijun = kijunSen.getValue(lastIndex).doubleValue();
        double price = close.getValue(lastIndex).doubleValue();

        log.info("üìä Ichimoku: price={}, tenkan={}, kijun={}", price, tenkan, kijun);

        // –õ–æ–≥–∏–∫–∞ –ø—Ä–∏–Ω—è—Ç–∏—è —Ä–µ—à–µ–Ω–∏—è –Ω–∞ –æ—Å–Ω–æ–≤–µ Ichimoku
        if (tenkan > kijun && price > tenkan) {
            return Signal.BUY;
        } else if (tenkan < kijun && price < tenkan) {
            return Signal.SELL;
        } else {
            return Signal.HOLD;
        }
    }

    @Override
    public void setConfig(Object config) {
        if (config instanceof IchimokuConfig ichimokuConfig) {
            this.config = ichimokuConfig;
        }
    }

    @Override
    public void execute() {
        // –í—ã–ø–æ–ª–Ω–µ–Ω–∏–µ —Å—Ç—Ä–∞—Ç–µ–≥–∏–∏: –∞–Ω–∞–ª–∏–∑–∏—Ä—É–µ–º –ø–æ—Å–ª–µ–¥–Ω–∏–µ –¥–∞–Ω–Ω—ã–µ –∏ –ø—Ä–∏–Ω–∏–º–∞–µ–º —Ä–µ—à–µ–Ω–∏–µ
        log.info("–ó–∞–ø—É—Å–∫ —Å—Ç—Ä–∞—Ç–µ–≥–∏–∏ Ichimoku...");

        // –ü–æ–ª—É—á–∞–µ–º –ø–æ—Å–ª–µ–¥–Ω–∏–µ –¥–∞–Ω–Ω—ã–µ –¥–ª—è –∞–Ω–∞–ª–∏–∑–∞ (–Ω–∞–ø—Ä–∏–º–µ—Ä, 100 —Å–≤–µ—á–µ–π)
        List<MarketCandle> latestCandles = new ArrayList<>();  // –ü–æ–ª—É—á–∏—Ç–µ –ø–æ—Å–ª–µ–¥–Ω–∏–µ —Å–≤–µ—á–∏ —Å –≤–∞—à–µ–≥–æ —Å–µ—Ä–≤–∏—Å–∞
        // –ü—Ä–∏–º–µ—Ä: latestCandles = candleService.getLatestCandles("BTCUSDT", "1h", 100);

        // –í—ã–ø–æ–ª–Ω—è–µ–º –∞–Ω–∞–ª–∏–∑ –∏ –ø–æ–ª—É—á–∞–µ–º —Ä–µ—à–µ–Ω–∏–µ
        Signal decision = analyze(latestCandles);

        // –õ–æ–≥–∏–∫–∞ –¥–ª—è –≤—ã–ø–æ–ª–Ω–µ–Ω–∏—è –æ—Ä–¥–µ—Ä–∞ –≤ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏ –æ—Ç —Å–∏–≥–Ω–∞–ª–∞
        if (decision == Signal.BUY) {
            log.info("üí° –°–∏–≥–Ω–∞–ª –Ω–∞ –ø–æ–∫—É–ø–∫—É. –†–∞–∑–º–µ—â–µ–Ω–∏–µ –æ—Ä–¥–µ—Ä–∞...");
            // –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –æ—Ä–¥–µ—Ä –Ω–∞ –ø–æ–∫—É–ø–∫—É
            // orderService.placeBuyOrder(chatId, "BTCUSDT", qty, true);
            // –£–≤–µ–¥–æ–º–ª—è–µ–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
            // notificationService.sendTradeNotification(chatId, Signal.BUY);
        } else if (decision == Signal.SELL) {
            log.info("üí° –°–∏–≥–Ω–∞–ª –Ω–∞ –ø—Ä–æ–¥–∞–∂—É. –†–∞–∑–º–µ—â–µ–Ω–∏–µ –æ—Ä–¥–µ—Ä–∞...");
            // –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –æ—Ä–¥–µ—Ä –Ω–∞ –ø—Ä–æ–¥–∞–∂—É
            // orderService.placeSellOrder(chatId, "BTCUSDT", qty, true);
            // –£–≤–µ–¥–æ–º–ª—è–µ–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
            // notificationService.sendTradeNotification(chatId, Signal.SELL);
        } else {
            log.info("‚ö™Ô∏è –°–∏–≥–Ω–∞–ª –Ω–∞ —É–¥–µ—Ä–∂–∞–Ω–∏–µ. –°–¥–µ–ª–∫–∞ –Ω–µ —Ä–∞–∑–º–µ—â–µ–Ω–∞.");
        }
    }
}
