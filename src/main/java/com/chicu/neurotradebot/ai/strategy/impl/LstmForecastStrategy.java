@Slf4j
@Component
@RequiredArgsConstructor
public class LstmForecastStrategy implements AiStrategy {

    private final MarketCandleService candleService;
    private final PricePredictor predictor; // –î–ª—è –ø—Ä–µ–¥—Å–∫–∞–∑–∞–Ω–∏—è —Ü–µ–Ω —Å –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ–º –º–æ–¥–µ–ª–∏ LSTM
    private final OrderService orderService; // –î–ª—è —Ä–∞–∑–º–µ—â–µ–Ω–∏—è –æ—Ä–¥–µ—Ä–æ–≤
    private final TelegramNotificationService notificationService; // –î–ª—è –æ—Ç–ø—Ä–∞–≤–∫–∏ —É–≤–µ–¥–æ–º–ª–µ–Ω–∏–π
    private LstmConfig config = new LstmConfig(); // –ö–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—è –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é

    @Override
    public String getName() {
        return "LSTM Forecast";
    }

    @Override
    public Signal analyze(List<MarketCandle> candles) {
        if (candles.size() < config.getWindowSize()) {
            log.warn("üìâ –ù–µ–¥–æ—Å—Ç–∞—Ç–æ—á–Ω–æ –¥–∞–Ω–Ω—ã—Ö –¥–ª—è LSTM —Å—Ç—Ä–∞—Ç–µ–≥–∏–∏");
            return Signal.HOLD;  // –ï—Å–ª–∏ –¥–∞–Ω–Ω—ã—Ö –Ω–µ–¥–æ—Å—Ç–∞—Ç–æ—á–Ω–æ, –≤–æ–∑–≤—Ä–∞—â–∞–µ–º HOLD
        }

        // –°–æ—Å—Ç–∞–≤–ª—è–µ–º —Å–ø–∏—Å–æ–∫ –∏—Å—Ç–æ—Ä–∏—á–µ—Å–∫–∏—Ö –¥–∞–Ω–Ω—ã—Ö –¥–ª—è –ø—Ä–æ–≥–Ω–æ–∑–∞
        List<Double> history = new ArrayList<>();
        for (int i = candles.size() - config.getWindowSize(); i < candles.size(); i++) {
            history.add(candles.get(i).getClose()); // –ó–∞–∫—Ä—ã–≤–∞—é—â–∏–µ —Ü–µ–Ω—ã
        }

        // –ü—Ä–æ–≥–Ω–æ–∑–∏—Ä—É–µ–º —Å–ª–µ–¥—É—é—â—É—é —Ü–µ–Ω—É
        double forecast = predictor.predictNextPrice(history);
        double lastPrice = candles.get(candles.size() - 1).getClose(); // –¢–µ–∫—É—â–∞—è —Ü–µ–Ω–∞

        log.info("ü§ñ LSTM: –ø—Ä–æ–≥–Ω–æ–∑={}, —Ç–µ–∫—É—â–∞—è —Ü–µ–Ω–∞={}", forecast, lastPrice);

        // –õ–æ–≥–∏–∫–∞ –ø—Ä–∏–Ω—è—Ç–∏—è —Ä–µ—à–µ–Ω–∏—è: –ø–æ–∫—É–ø–∫–∞, –ø—Ä–æ–¥–∞–∂–∞ –∏–ª–∏ —É–¥–µ—Ä–∂–∞–Ω–∏–µ
        if (forecast > lastPrice * 1.002) return Signal.BUY;  // –ü—Ä–æ–≥–Ω–æ–∑–∏—Ä—É–µ–º–∞—è —Ü–µ–Ω–∞ –≤—ã—à–µ –Ω–∞ 0.2% ‚Äî —Å–∏–≥–Ω–∞–ª –Ω–∞ –ø–æ–∫—É–ø–∫—É
        if (forecast < lastPrice * 0.998) return Signal.SELL; // –ü—Ä–æ–≥–Ω–æ–∑–∏—Ä—É–µ–º–∞—è —Ü–µ–Ω–∞ –Ω–∏–∂–µ –Ω–∞ 0.2% ‚Äî —Å–∏–≥–Ω–∞–ª –Ω–∞ –ø—Ä–æ–¥–∞–∂—É
        return Signal.HOLD;  // –ï—Å–ª–∏ —Ü–µ–Ω–∞ –Ω–µ –∏–∑–º–µ–Ω–∏–ª–∞—Å—å —Å—É—â–µ—Å—Ç–≤–µ–Ω–Ω–æ, —É–¥–µ—Ä–∂–∏–≤–∞–µ–º –ø–æ–∑–∏—Ü–∏—é
    }

    @Override
    public void setConfig(Object config) {
        if (config instanceof LstmConfig lstmConfig) {
            this.config = lstmConfig;
        } else {
            log.warn("‚ùå –ù–µ–≤–µ—Ä–Ω–∞—è –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—è –ø–µ—Ä–µ–¥–∞–Ω–∞ –≤ LSTM —Å—Ç—Ä–∞—Ç–µ–≥–∏—é: {}", config);
        }
    }

    @Override
    public void execute() {
        // –í—ã–ø–æ–ª–Ω–µ–Ω–∏–µ —Å—Ç—Ä–∞—Ç–µ–≥–∏–∏: –∞–Ω–∞–ª–∏–∑–∏—Ä—É–µ–º –ø–æ—Å–ª–µ–¥–Ω–∏–µ –¥–∞–Ω–Ω—ã–µ –∏ –ø—Ä–∏–Ω–∏–º–∞–µ–º —Ä–µ—à–µ–Ω–∏–µ
        log.info("–ó–∞–ø—É—Å–∫ —Å—Ç—Ä–∞—Ç–µ–≥–∏–∏ LSTM Forecast...");

        // –ü–æ–ª—É—á–∞–µ–º –ø–æ—Å–ª–µ–¥–Ω–∏–µ –¥–∞–Ω–Ω—ã–µ –¥–ª—è –∞–Ω–∞–ª–∏–∑–∞ (–Ω–∞–ø—Ä–∏–º–µ—Ä, 100 —Å–≤–µ—á–µ–π)
        List<MarketCandle> latestCandles = new ArrayList<>();  // –ü–æ–ª—É—á–∏—Ç–µ –ø–æ—Å–ª–µ–¥–Ω–∏–µ —Å–≤–µ—á–∏ —Å –≤–∞—à–µ–≥–æ —Å–µ—Ä–≤–∏—Å–∞
        // –ü—Ä–∏–º–µ—Ä: latestCandles = candleService.getLatestCandles("BTCUSDT", "1h", 100);

        // –í—ã–ø–æ–ª–Ω—è–µ–º –∞–Ω–∞–ª–∏–∑ –∏ –ø–æ–ª—É—á–∞–µ–º —Ä–µ—à–µ–Ω–∏–µ
        Signal decision = analyze(latestCandles);

        // –õ–æ–≥–∏–∫–∞ –¥–ª—è –≤—ã–ø–æ–ª–Ω–µ–Ω–∏—è –æ—Ä–¥–µ—Ä–∞ –≤ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏ –æ—Ç —Å–∏–≥–Ω–∞–ª–∞
        if (decision == Signal.BUY) {
            log.info("üí° –°–∏–≥–Ω–∞–ª –Ω–∞ –ø–æ–∫—É–ø–∫—É. –†–∞–∑–º–µ—â–µ–Ω–∏–µ –æ—Ä–¥–µ—Ä–∞...");
            // –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –æ—Ä–¥–µ—Ä –Ω–∞ –ø–æ–∫—É–ø–∫—É
            // orderService.placeBuyOrder(chatId, "BTCUSDT", qty, true);
            // –£–≤–µ–¥–æ–º–ª—è–µ–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
            // notificationService.sendTradeNotification(chatId, Signal.BUY);
        } else if (decision == Signal.SELL) {
            log.info("üí° –°–∏–≥–Ω–∞–ª –Ω–∞ –ø—Ä–æ–¥–∞–∂—É. –†–∞–∑–º–µ—â–µ–Ω–∏–µ –æ—Ä–¥–µ—Ä–∞...");
            // –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –æ—Ä–¥–µ—Ä –Ω–∞ –ø—Ä–æ–¥–∞–∂—É
            // orderService.placeSellOrder(chatId, "BTCUSDT", qty, true);
            // –£–≤–µ–¥–æ–º–ª—è–µ–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
            // notificationService.sendTradeNotification(chatId, Signal.SELL);
        } else {
            log.info("‚ö™Ô∏è –°–∏–≥–Ω–∞–ª –Ω–∞ —É–¥–µ—Ä–∂–∞–Ω–∏–µ. –°–¥–µ–ª–∫–∞ –Ω–µ —Ä–∞–∑–º–µ—â–µ–Ω–∞.");
        }
    }
}
